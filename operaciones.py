def sumar(num1, num2):
	return num1+num2

def restar(num1, num2):
	return num1-num2

def mult(num1, num2):
	return num1*num2

def div(num1, num2):
	return num1/num2

num1 = 7
num2 = 4
print('Suma', sumar(num1, num2))
print('Resta', restar(num1, num2))
print('Multiplicacion', mult(num1, num2))
print('Division', div(num1, num2))